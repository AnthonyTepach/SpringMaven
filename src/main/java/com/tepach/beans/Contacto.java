package com.tepach.beans;

/**
 *
 * @author Anthony Tepach
 */
public class Contacto {

    private String lada, num_casa, num_movil, email;

    public String getLada() {
        return lada;
    }

    public void setLada(String lada) {
        this.lada = lada;
    }

    public String getNum_casa() {
        return num_casa;
    }

    public void setNum_casa(String num_casa) {
        this.num_casa = num_casa;
    }

    public String getNum_movil() {
        return num_movil;
    }

    public void setNum_movil(String num_movil) {
        this.num_movil = num_movil;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }
}
